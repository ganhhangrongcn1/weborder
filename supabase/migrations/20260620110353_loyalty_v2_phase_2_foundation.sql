-- Điểm danh: daily và milestone được quyết định tại database trong cùng transaction.
create or replace function loyalty_private.checkin_internal(
  p_idempotency_key text
)
returns table (
  ok boolean,
  applied boolean,
  event_id text,
  action text,
  points_delta integer,
  balance_before integer,
  balance_after integer,
  checkin_streak integer,
  message text
)
language plpgsql
security definer
set search_path = pg_catalog, public, loyalty_private, auth
as $$
declare
  v_actor_type text;
  v_actor_id uuid;
  v_phone text;
  v_actor_role text;
  v_rule public.loyalty_rule_versions%rowtype;
  v_business_date date := timezone('Asia/Ho_Chi_Minh', now())::date;
  v_last_date date;
  v_streak integer;
  v_daily_points integer;
  v_milestone_points integer := 0;
  v_balance_before integer;
  v_balance_after integer;
  v_event_id text;
  v_milestone_id text;
  v_inserted_id text;
  v_idempotency_key text := trim(coalesce(p_idempotency_key, ''));
  v_existing public.loyalty_ledger%rowtype;
begin
  if v_idempotency_key = '' or length(v_idempotency_key) > 200 then
    raise exception 'Idempotency key không hợp lệ.';
  end if;

  select *
  into v_actor_type, v_actor_id, v_phone, v_actor_role
  from loyalty_private.current_actor();

  if v_actor_role <> 'customer' or v_phone = '' then
    raise exception 'Chỉ khách hàng đã xác thực mới được điểm danh.';
  end if;

  select * into v_rule
  from public.loyalty_rule_versions
  where status = 'ACTIVE'
    and effective_from <= now()
  order by effective_from desc, version_number desc
  limit 1;

  if not found or v_rule.checkin_daily_points <= 0 then
    raise exception 'Tính năng điểm danh hiện chưa được bật.';
  end if;

  insert into public.loyalty_accounts (customer_phone, total_points, metadata)
  values (v_phone, 0, jsonb_build_object('source', 'loyalty_v2_checkin'))
  on conflict (customer_phone) do nothing;

  select
    coalesce(la.total_points, 0)::integer,
    case
      when coalesce(la.last_checkin_date, '') ~ '^\d{4}-\d{2}-\d{2}$'
      then la.last_checkin_date::date
      else null
    end,
    coalesce(la.checkin_streak, 0)
  into v_balance_before, v_last_date, v_streak
  from public.loyalty_accounts la
  where la.customer_phone = v_phone
  for update;

  select * into v_existing
  from public.loyalty_ledger ll
  where ll.source_type = 'CUSTOMER_DAY'
    and ll.source_order_id = v_phone || ':' || v_business_date::text
    and ll.action = 'CHECKIN'
    and ll.action_version = 1
  limit 1;

  if found then
    return query select
      true, false, v_existing.id, 'CHECKIN', v_existing.points,
      v_balance_before, v_balance_before, v_streak,
      'Hôm nay khách đã điểm danh.';
    return;
  end if;

  if v_last_date = v_business_date - 1 then
    v_streak := v_streak + 1;
  else
    v_streak := 1;
  end if;

  v_daily_points := v_rule.checkin_daily_points;
  if coalesce(v_rule.streak_rewards ->> v_streak::text, '') ~ '^\d+$' then
    v_milestone_points := greatest((v_rule.streak_rewards ->> v_streak::text)::integer, 0);
  end if;

  v_event_id := 'loyalty-v2-checkin-' || gen_random_uuid()::text;

  insert into public.loyalty_ledger (
    id, customer_phone, entry_type, order_id, points, amount, title, note,
    metadata, source, source_type, source_order_id, action, action_version,
    idempotency_key, rule_version_id, actor_type, actor_id, created_at
  ) values (
    v_event_id,
    v_phone,
    'CHECKIN_V2',
    null,
    v_daily_points,
    0,
    'Điểm danh hằng ngày',
    'Ngày ' || to_char(v_business_date, 'DD/MM/YYYY'),
    jsonb_build_object(
      'source', 'loyalty_v2',
      'businessDate', v_business_date,
      'streak', v_streak
    ),
    'checkin',
    'CUSTOMER_DAY',
    v_phone || ':' || v_business_date::text,
    'CHECKIN',
    1,
    v_idempotency_key,
    v_rule.id,
    v_actor_type,
    v_actor_id,
    now()
  )
  on conflict do nothing
  returning id into v_inserted_id;

  if v_inserted_id is null then
    select * into v_existing
    from public.loyalty_ledger ll
    where ll.source_type = 'CUSTOMER_DAY'
      and ll.source_order_id = v_phone || ':' || v_business_date::text
      and ll.action = 'CHECKIN'
      and ll.action_version = 1
    limit 1;

    if not found then
      if exists (
        select 1
        from public.loyalty_ledger ll
        where ll.idempotency_key = v_idempotency_key
      ) then
        raise exception 'Idempotency key đã được dùng cho một sự kiện khác.';
      end if;
      raise exception 'Không thể tạo sự kiện điểm danh do xung đột dữ liệu.';
    end if;

    select coalesce(total_points, 0)::integer
    into v_balance_after
    from public.loyalty_accounts
    where customer_phone = v_phone;

    return query select
      true, false, v_existing.id, 'CHECKIN', v_existing.points,
      v_balance_before, v_balance_after, v_streak,
      'Hôm nay khách đã điểm danh.';
    return;
  end if;

  if v_milestone_points > 0 then
    v_milestone_id := 'loyalty-v2-milestone-' || gen_random_uuid()::text;
    insert into public.loyalty_ledger (
      id, customer_phone, entry_type, order_id, points, amount, title, note,
      metadata, source, source_type, source_order_id, action, action_version,
      idempotency_key, rule_version_id, actor_type, actor_id, created_at
    ) values (
      v_milestone_id,
      v_phone,
      'MILESTONE',
      null,
      v_milestone_points,
      0,
      'Thưởng chuỗi điểm danh',
      'Mốc ' || v_streak::text || ' ngày liên tiếp',
      jsonb_build_object(
        'source', 'loyalty_v2',
        'businessDate', v_business_date,
        'streak', v_streak
      ),
      'checkin',
      'CUSTOMER_STREAK',
      v_phone || ':' || v_business_date::text || ':' || v_streak::text,
      'MILESTONE',
      1,
      v_idempotency_key || ':milestone:' || v_streak::text,
      v_rule.id,
      v_actor_type,
      v_actor_id,
      now()
    )
    on conflict do nothing;
  end if;

  update public.loyalty_accounts
  set
    checkin_streak = v_streak,
    last_checkin_date = v_business_date::text,
    updated_at = now()
  where customer_phone = v_phone;

  select coalesce(total_points, 0)::integer
  into v_balance_after
  from public.loyalty_accounts
  where customer_phone = v_phone;

  return query select
    true, true, v_event_id, 'CHECKIN', v_daily_points + v_milestone_points,
    v_balance_before, v_balance_after, v_streak,
    case
      when v_milestone_points > 0
      then 'Điểm danh và nhận thưởng mốc thành công.'
      else 'Điểm danh thành công.'
    end;
end;
$$;

