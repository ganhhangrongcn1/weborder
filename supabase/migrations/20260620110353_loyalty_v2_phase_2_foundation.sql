-- Kích hoạt phiên bản rule mới. Chỉ admin, không tính lại đơn cũ.
create or replace function loyalty_private.activate_rule_internal(
  p_earn_numerator bigint,
  p_earn_denominator bigint,
  p_redeem_point_unit bigint,
  p_redeem_value bigint,
  p_checkin_daily_points integer,
  p_streak_rewards jsonb,
  p_idempotency_key text
)
returns table (
  ok boolean,
  rule_version_id uuid,
  version_number bigint,
  message text
)
language plpgsql
security definer
set search_path = pg_catalog, public, loyalty_private, auth
as $$
declare
  v_actor_type text;
  v_actor_id uuid;
  v_actor_phone text;
  v_actor_role text;
  v_key text := trim(coalesce(p_idempotency_key, ''));
  v_existing public.loyalty_rule_versions%rowtype;
  v_new public.loyalty_rule_versions%rowtype;
  v_config jsonb;
begin
  select *
  into v_actor_type, v_actor_id, v_actor_phone, v_actor_role
  from loyalty_private.current_actor();

  if v_actor_role <> 'admin' then
    raise exception 'Chỉ admin được thay đổi quy tắc loyalty.';
  end if;
  if coalesce(p_earn_numerator, 0) <= 0 or coalesce(p_earn_denominator, 0) <= 0 then
    raise exception 'Tỷ lệ tích điểm phải lớn hơn 0.';
  end if;
  if coalesce(p_redeem_point_unit, 0) <= 0 or coalesce(p_redeem_value, 0) <= 0 then
    raise exception 'Tỷ lệ đổi điểm phải lớn hơn 0.';
  end if;
  if coalesce(p_checkin_daily_points, 0) < 0 then
    raise exception 'Điểm danh hằng ngày không được âm.';
  end if;
  if v_key = '' or length(v_key) > 200 then
    raise exception 'Idempotency key không hợp lệ.';
  end if;

  select * into v_existing
  from public.loyalty_rule_versions
  where source_config ->> 'idempotencyKey' = v_key
  order by created_at
  limit 1;

  if found then
    return query select true, v_existing.id, v_existing.version_number,
      'Phiên bản quy tắc đã được tạo trước đó.';
    return;
  end if;

  update public.loyalty_rule_versions
  set status = 'RETIRED', retired_at = now()
  where status = 'ACTIVE';

  v_config := jsonb_build_object(
    'enabled', true,
    'currencyPerPoint', p_earn_denominator,
    'pointPerUnit', p_earn_numerator,
    'redeemPointUnit', p_redeem_point_unit,
    'redeemValue', p_redeem_value,
    'checkinDailyPoints', p_checkin_daily_points,
    'streakRewards', coalesce(p_streak_rewards, '{}'::jsonb),
    'idempotencyKey', v_key,
    'source', 'loyalty_v2'
  );

  insert into public.loyalty_rule_versions (
    status,
    effective_from,
    earn_numerator,
    earn_denominator,
    redeem_point_unit,
    redeem_value,
    checkin_daily_points,
    streak_rewards,
    source_config,
    created_by
  ) values (
    'ACTIVE',
    now(),
    p_earn_numerator,
    p_earn_denominator,
    p_redeem_point_unit,
    p_redeem_value,
    p_checkin_daily_points,
    coalesce(p_streak_rewards, '{}'::jsonb),
    v_config,
    auth.uid()
  )
  returning * into v_new;

  insert into public.app_configs (id, value, updated_at)
  values ('ghr_loyalty', v_config, now())
  on conflict (id) do update
  set
    value = coalesce(public.app_configs.value, '{}'::jsonb) || excluded.value,
    updated_at = now();

  return query select true, v_new.id, v_new.version_number,
    'Đã kích hoạt quy tắc mới; đơn cũ giữ nguyên snapshot.';
end;
$$;

