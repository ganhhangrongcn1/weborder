  id uuid primary key default gen_random_uuid(),
  version_number bigint generated by default as identity,
  status text not null default 'DRAFT',
  effective_from timestamptz not null default now(),
  earn_numerator bigint not null,
  earn_denominator bigint not null,
  redeem_point_unit bigint not null default 1,
  redeem_value bigint not null default 1,
  checkin_daily_points integer not null default 0,
  streak_rewards jsonb not null default '{}'::jsonb,
  source_config jsonb not null default '{}'::jsonb,
  created_by uuid null references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  retired_at timestamptz null,
  constraint loyalty_rule_versions_version_number_key unique (version_number),
  constraint loyalty_rule_versions_status_check
    check (status in ('DRAFT', 'ACTIVE', 'RETIRED')),
  constraint loyalty_rule_versions_earn_check
    check (earn_numerator > 0 and earn_denominator > 0),
  constraint loyalty_rule_versions_redeem_check
    check (redeem_point_unit > 0 and redeem_value > 0),
  constraint loyalty_rule_versions_checkin_check
    check (checkin_daily_points >= 0)
);

