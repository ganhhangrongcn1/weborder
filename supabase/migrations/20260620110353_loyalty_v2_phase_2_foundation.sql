-- Migration phải tự dừng nếu nền tảng không đạt các bất biến quan trọng.
do $$
declare
  v_count bigint;
  v_account_total bigint;
  v_ledger_total bigint;
begin
  select count(*) into v_count
  from public.loyalty_rule_versions
  where status = 'ACTIVE';
  if v_count <> 1 then
    raise exception 'Loyalty V2 cần đúng một rule ACTIVE, hiện có %.', v_count;
  end if;

  select count(*) into v_count
  from public.orders
  where loyalty_rule_version_id is null
     or points_base_amount is null
     or expected_earn_points is null
     or points_spent is null
     or points_discount_amount is null;
  if v_count <> 0 then
    raise exception 'Còn % orders thiếu snapshot Loyalty V2.', v_count;
  end if;

  select count(*) into v_count
  from public.partner_orders
  where loyalty_rule_version_id is null
     or expected_earn_points is null
     or points_spent is null
     or points_discount_amount is null;
  if v_count <> 0 then
    raise exception 'Còn % partner_orders thiếu snapshot Loyalty V2.', v_count;
  end if;

  select coalesce(sum(total_points), 0)::bigint
  into v_account_total
  from public.loyalty_accounts;

  select coalesce(sum(points), 0)::bigint
  into v_ledger_total
  from public.loyalty_ledger;

  if v_account_total <> v_ledger_total then
    raise exception 'Tổng account % không khớp ledger %.', v_account_total, v_ledger_total;
  end if;

  if to_regprocedure('public.process_order_loyalty(text,text,text,text)') is null
    or to_regprocedure('public.process_loyalty_checkin(text)') is null
    or to_regprocedure('public.admin_adjust_loyalty_points(text,integer,text,text)') is null
    or to_regprocedure('public.activate_loyalty_rule_version(bigint,bigint,bigint,bigint,integer,jsonb,text)') is null
  then
    raise exception 'Thiếu một hoặc nhiều RPC Loyalty V2.';
  end if;

  if has_function_privilege(
    'anon',
    'public.process_order_loyalty(text,text,text,text)',
    'EXECUTE'
  ) then
    raise exception 'anon không được có quyền gọi process_order_loyalty.';
  end if;
end;
$$;

