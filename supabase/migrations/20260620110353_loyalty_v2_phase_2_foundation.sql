-- Số dư phải luôn bằng tổng ledger. REVERSE_EARN có thể tạo số dư âm (nợ điểm)
-- nếu khách đã tiêu điểm trước khi đơn bị hoàn/hủy.
create or replace function public.recompute_loyalty_account(p_phone text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_phone text := btrim(coalesce(p_phone, ''));
  v_total integer := 0;
begin
  if v_phone = '' then
    return;
  end if;

  select coalesce(sum(points), 0)::integer
  into v_total
  from public.loyalty_ledger
  where btrim(customer_phone) = v_phone;

  insert into public.loyalty_accounts (customer_phone, total_points, updated_at)
  values (v_phone, v_total, now())
  on conflict (customer_phone) do update
  set total_points = excluded.total_points,
      updated_at = now();
end;
$$;

create or replace function public.trg_lock_loyalty_accounts_total_points()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_total integer := 0;
begin
  select coalesce(sum(points), 0)::integer
  into v_total
  from public.loyalty_ledger
  where btrim(customer_phone) = btrim(new.customer_phone);

  new.customer_phone := btrim(new.customer_phone);
  new.total_points := v_total;
  new.updated_at := now();
  return new;
end;
$$;

-- Helper đọc số nguyên không âm từ metadata mà không gây lỗi cast.
create or replace function loyalty_private.jsonb_nonnegative_integer(
  p_value jsonb,
  p_key text
)
returns integer
language plpgsql
immutable
set search_path = pg_catalog
as $$
declare
  v_text text;
begin
  v_text := trim(coalesce(p_value ->> p_key, ''));
  if v_text !~ '^\d+(\.\d+)?$' then
    return null;
  end if;
  return greatest(floor(v_text::numeric)::integer, 0);
exception
  when numeric_value_out_of_range then
    return null;
end;
$$;

-- Snapshot rule cho đơn mới. Chỉ ghi các cột V2, không đổi tổng tiền/status legacy.
create or replace function loyalty_private.snapshot_order_loyalty_rule()
returns trigger
language plpgsql
security definer
set search_path = pg_catalog, public, loyalty_private
as $$
declare
  v_rule public.loyalty_rule_versions%rowtype;
  v_metadata_points integer;
begin
  select * into v_rule
  from public.loyalty_rule_versions
  where status = 'ACTIVE'
    and effective_from <= now()
  order by effective_from desc, version_number desc
  limit 1;

  if not found then
    raise exception 'Chưa có phiên bản quy tắc loyalty đang hoạt động.';
  end if;

  new.loyalty_rule_version_id := v_rule.id;
  new.points_base_amount := greatest(
    coalesce(new.subtotal, 0) - coalesce(new.promo_discount, 0),
    0
  );
  new.expected_earn_points := floor(
    greatest(new.points_base_amount, 0) * v_rule.earn_numerator / v_rule.earn_denominator
  )::integer;
  new.points_discount_amount := coalesce(new.points_discount, 0);

  v_metadata_points := loyalty_private.jsonb_nonnegative_integer(new.metadata, 'pointsSpent');
  new.points_spent := coalesce(new.points_spent, v_metadata_points);

  if new.points_spent is null
    and coalesce(new.points_discount, 0) > 0
    and v_rule.redeem_point_unit = v_rule.redeem_value
  then
    new.points_spent := floor(new.points_discount)::integer;
  end if;

  new.points_spent := coalesce(new.points_spent, 0);
  return new;
end;
$$;

