-- Admin adjustment luôn tạo ledger, không sửa thẳng total_points.
create or replace function loyalty_private.admin_adjust_internal(
  p_customer_phone text,
  p_points integer,
  p_reason text,
  p_idempotency_key text
)
returns table (
  ok boolean,
  applied boolean,
  event_id text,
  action text,
  points_delta integer,
  balance_before integer,
  balance_after integer,
  message text
)
language plpgsql
security definer
set search_path = pg_catalog, public, loyalty_private, auth
as $$
declare
  v_actor_type text;
  v_actor_id uuid;
  v_actor_phone text;
  v_actor_role text;
  v_phone text := public.normalize_vietnam_phone(p_customer_phone);
  v_reason text := trim(coalesce(p_reason, ''));
  v_key text := trim(coalesce(p_idempotency_key, ''));
  v_before integer;
  v_after integer;
  v_event_id text;
  v_existing public.loyalty_ledger%rowtype;
begin
  select *
  into v_actor_type, v_actor_id, v_actor_phone, v_actor_role
  from loyalty_private.current_actor();

  if v_actor_role <> 'admin' then
    raise exception 'Chỉ admin được điều chỉnh điểm.';
  end if;
  if v_phone = '' then
    raise exception 'Số điện thoại khách không hợp lệ.';
  end if;
  if coalesce(p_points, 0) = 0 then
    raise exception 'Số điểm điều chỉnh phải khác 0.';
  end if;
  if length(v_reason) < 3 then
    raise exception 'Bắt buộc nhập lý do điều chỉnh.';
  end if;
  if v_key = '' or length(v_key) > 200 then
    raise exception 'Idempotency key không hợp lệ.';
  end if;

  insert into public.profiles (phone, registered, role, status, metadata)
  values (v_phone, false, 'customer', 'active', jsonb_build_object('source', 'loyalty_v2_admin'))
  on conflict (phone) do nothing;

  insert into public.loyalty_accounts (customer_phone, total_points, metadata)
  values (v_phone, 0, jsonb_build_object('source', 'loyalty_v2_admin'))
  on conflict (customer_phone) do nothing;

  select coalesce(total_points, 0)::integer
  into v_before
  from public.loyalty_accounts
  where customer_phone = v_phone
  for update;

  select * into v_existing
  from public.loyalty_ledger
  where idempotency_key = v_key
  limit 1;

  if found then
    return query select
      true, false, v_existing.id, 'ADMIN_ADJUST', v_existing.points,
      v_before, v_before, 'Yêu cầu đã được xử lý trước đó.';
    return;
  end if;

  if v_before + p_points < 0 then
    raise exception 'Điều chỉnh làm số dư âm. Hiện có %, thay đổi %.', v_before, p_points;
  end if;

  v_event_id := 'loyalty-v2-admin-' || gen_random_uuid()::text;
  insert into public.loyalty_ledger (
    id, customer_phone, entry_type, order_id, points, amount, title, note,
    metadata, source, source_type, source_order_id, action, action_version,
    idempotency_key, actor_type, actor_id, created_at
  ) values (
    v_event_id,
    v_phone,
    'ADMIN_ADJUST',
    null,
    p_points,
    0,
    'Admin điều chỉnh điểm',
    v_reason,
    jsonb_build_object('source', 'loyalty_v2', 'reason', v_reason),
    'admin',
    'ADMIN',
    v_key,
    'ADMIN_ADJUST',
    1,
    v_key,
    v_actor_type,
    v_actor_id,
    now()
  );

  select coalesce(total_points, 0)::integer
  into v_after
  from public.loyalty_accounts
  where customer_phone = v_phone;

  return query select
    true, true, v_event_id, 'ADMIN_ADJUST', p_points,
    v_before, v_after, 'Điều chỉnh điểm thành công.';
end;
